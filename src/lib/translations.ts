export type Language = keyof typeof translations;

export function useTranslation(defaultLang: Language = 'en') {
  const [lang, setLangState] = useState<Language>(defaultLang);

  // Load saved language on mount
  useEffect(() => {
    const storedLang = localStorage.getItem('agriai_lang') as Language;
    if (storedLang && translations[storedLang]) {
      setLangState(storedLang);
    }
  }, []);

  const setLang = (newLang: Language) => {
    if (translations[newLang]) {
      setLangState(newLang);
      localStorage.setItem('agriai_lang', newLang);
    }
  };

  return {
    lang,
    setLang,
    t: { ...translations.en, ...(translations[lang] || {}) }
  };
}
